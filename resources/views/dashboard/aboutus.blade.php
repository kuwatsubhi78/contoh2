                    <div class="content" style="text-align: justify;">
                        <p>
                            Dalam upaya ikut berperan serta membangun dan memajukan bangsa Indonesia terutama dalam
                            bidang kesehatan, pendiri Yayasan Ngudi Waluyo Ungaran, BapakH. Asaat Pitoyo, S.Kp., M.Kes.,
                            sebagai terdorong untuk menyelenggarakan pendidikan tenaga kesehatan. Berdasar SK Menkes
                            No.H.K.00.06.1.1.2298 tanggal 1 Juni 1994 Yayasan Ngudi Waluyo Ungaran diperkenankan
                            menyelenggarakan Akademi Keperawatan yang lebih dikenal dengan nama AKPER Ngudi Waluyo
                            Ungaran, dengan peserta didik awalnya 40 Mahasiswa.
                        </p>

                        <p>
                            Pada awalnya dalam mengemban misinya, Yayasan Ngudi Waluyo Ungaran dalam melaksanakan proses
                            belajar-mengajar masih menggunakan gedung milik IKIP Negeri Semarang yang terletak di Jalan
                            Merapi No. 11 Suwakul Ungaran Jawa Tengah. Kemudian pada tahun 1996 proses belajar-mengajar
                            telah menempati lokasi serta gedung baru yang merupakan milik sendiri, berlokasi di Jl.
                            Gedongsongo, Mijen Telp. (024) 6925406, 6925407, 6925408 Ungaran – Jawa Tengah, dengan luas
                            + 12000 m2. Lokasi baru tersebut telah pula dilengkapi beberapa fasilitas untuk menunjang
                            proses belajar-mengajar.
                        </p>

                        <p>
                            Disamping itu, dengan semakin lengkapnya fasilitas dan kualitas pendidikannya, maka pada
                            tahun 1998 Akademi Keperawatan Ngudi Waluyo mendapatkan predikat Akreditasi “B” berdasar SK
                            Menkes No.00.06.4.3.4843 tanggal 8 September 1998. Atas komitmen tinggi dan pengelolaan yang
                            profesional, dalam perkembangannya tiga tahun kemudian, akademi ini mendapatkan kepercayaan
                            dari Pusdiknakes(Pusat Pendidikan Tenaga Kesehatan) Depkes RI untuk menyandang predikat
                            Akreditasi “A” berdasar SK Pusdiknakes Depkes RI. No.HK.00.06.2.4.01577.
                        </p>


                        <img src="/gambar-slider/1720308843_1 - GEDUNG C.JPG" class="img-fluid" alt="">

                        <p>
                            Dengan keyakinan dan bermodalkan prestasi yang telah dicapai dalam penyelenggaraan
                            pendidikan tenaga kesehatan, serta dalam upaya ikut berperan aktif mengembangkan dunia
                            pendidikan yang disesuaikan untuk kepentingan nasional terutama dalam bidang tenaga
                            kesehatan, Yayasan Ngudi Waluyo Ungaran terdorong untuk mengembangkan sayap dan meningkatkan
                            jenjang pendidikan dari jenjang pendidikan Diploma (DIII) ke jenjang pendidikan strata satu
                            (S1) Kesehatan. Selain menyelenggarakan Akademi Kebidanan Ngudi Waluyo Ungaran, berdasar SK
                            Mendiknas No. 07/D/0/2001, Yayasan Ngudi Waluyo Ungaran juga menyelenggarakan Sekolah Tinggi
                            Ilmu Kesehatan (STIKES) Ngudi Waluyo berdasar SK Mendiknas No.39/D/0/2001 dengan 2 (dua)
                            Program Studi yaitu Program Studi Ilmu Kesehatan Masyarakat dan Program Studi Ilmu
                            Keperawatan. Dalam perkembangannya sesuai dengan SK Departemen Pendidikan Nasional Dirjen
                            Dikti No. 1956/D/T/2002 tanggal 16 September 2002, STIKES Ngudi Waluyo mulai tahun ajaran
                            2002 membuka satu lagi program studi yaitu Program Studi Kebidanan Jenjang Program Diploma
                            IV (D-IV) Bidan Pendidik.
                        </p>
                        <p>
                            Pada tahun 2004 STIKES Ngudi waluyo menambah program studi baru Diploma III Kebidanan
                            berdasarkan SK Departemen Pendidikan Nasional Dirjen Dikti No. 3850/D/T/2004.Satu tahun
                            kemudian tepatnya tahun 2005 STIKES Ngudi Waluyo menambah 2 program studi baru lagi yaitu
                            program studi Ilmu Gizi dan Farmasi jenjang pendidikan Strata Satu ( S-1) berdasarkan SK
                            Departemen Pendidikan Nasional Dirjen Dikti No. 1361/D/T/2005.
                        </p>
                  

